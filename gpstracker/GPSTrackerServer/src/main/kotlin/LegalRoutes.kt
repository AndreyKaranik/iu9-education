import io.ktor.http.*
import io.ktor.server.application.*
import io.ktor.server.response.*
import io.ktor.server.routing.*

fun Route.legalRoutes() {
    get("/privacy-policy") {
        call.respondText(
            """
            <!DOCTYPE html>
            <html lang="ru">
            <head>
                <meta charset="UTF-8">
                <title>Политика конфиденциальности</title>
            </head>
            <body>
                <h1>Политика конфиденциальности</h1>
                <p>
                    Мы соблюдаем конфиденциальность ваших данных. Ваше местоположение, сенсорные данные (акселерометр, гироскоп и т.п.) 
                    и фотографии используются только в рамках функциональности приложения.
                </p>
                <p>
                    Эти данные могут быть доступны другим пользователям <strong>только в рамках групп</strong>, 
                    в которые вы <strong>вступили добровольно</strong>. 
                    Таким образом, передача таких данных между участниками группы не считается передачей третьим лицам.
                </p>
                <p>
                    Ваши данные не передаются внешним сервисам, организациям или лицам без вашего согласия.
                </p>
            </body>
            </html>
            """.trimIndent(),
            ContentType.Text.Html
        )
    }

    get("/terms-of-use") {
        call.respondText(
            """
            <!DOCTYPE html>
            <html lang="ru">
            <head>
                <meta charset="UTF-8">
                <title>Пользовательское соглашение</title>
            </head>
            <body>
                <h1>Пользовательское соглашение</h1>
                <p>
                    Используя это приложение, вы соглашаетесь с условиями предоставления услуг. 
                    Вы обязуетесь использовать сервис законно и уважительно по отношению к другим пользователям.
                </p>
                <p>
                    Вы соглашаетесь, что другие участники группы, в которую вы вступили, могут видеть ваше местоположение и связанные данные.
                </p>
            </body>
            </html>
            """.trimIndent(),
            ContentType.Text.Html
        )
    }
}